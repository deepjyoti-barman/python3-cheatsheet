# Asterisk (*) as repetition operator (strings & lists)
# When used between a sequence (like a string or a list) and an integer, it repeats that sequence

# Print "meow" three times using repetition operator
print("meow\n" * 3, end="")

# Print list elements repeated twice using repetition operator
print([1, 2] * 2)  # [1, 2, 1, 2]
